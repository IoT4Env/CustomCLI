namespace CustomCLI;

public enum CliCommands
{
    Help, //shows all commands available
    Cls, //clear terminal
    Exit, //exit terminal
    Echo, //prints stuff on terminal
    Cd, //Climb directory
    Fd, //Fall directory
    Touch, //creates file
    Rm, //removes file
    Mkdir, //creates folder
    Rmdir, //removes directory and all files in it
    Ls, //list elements in current directory
}
